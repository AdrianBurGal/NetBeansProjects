package Tema_3;

public class Actividad_Resuelta_3_07 {

    public static void main(String[] args) {

        int resultado = 0;

        for (int numero = 0; resultado < 100; numero++) {
            resultado = numero * 7;

            if (resultado < 100) {

                System.out.println(resultado);

            }
        }

    }

}
